const setLectureName = (id, time) => {
    const element = document.getElementById(id);
    element.addEventListener('click', () => {
      const lectureName = prompt(`Enter the name of the lecture at ${time}`);
      element.innerText = lectureName;
    });
  }
  
  setLectureName('a', '7-8');
  setLectureName('b', '8-9');
  setLectureName('c', '9-10');
  setLectureName('d', '10-11');
